class Event {
  final String id;
  final String name;
  final String subName;
  final String description;
  final String location;
  final String time;
  final String date;

  Event({
    required this.id,
    required this.name,
    required this.subName,
    required this.description,
    required this.location,
    required this.time,
    required this.date,
  });
}
